    public void PuxaConta() {
        new Thread() {
            @Override
            public void run() {
                try {
                    api.QueryAcc(jTextField3.getText());
                } catch (NoSuchAlgorithmException | IOException | JSONException ex) {
                    front.setEnabled(true);
                    dispose();
                }
                jTextField1.setText(api.resultAcc);
                jTextField2.setText(api.result);
                idconta = api.resultAcc;
                idpersonagem = api.result;
            }
        }.start();
    }


